package org.launchcode.becflix.Models;

public class Show {
    private String title;
    private int year;
    private String actor;
    private String genre;
    private String rating;
    private String importance;

    public Show(){
        this.title = title;
        this.year = year;
        this.actor = actor;
        this.genre = rating;
        this.rating = rating;
        this.importance = importance;
    }
}
